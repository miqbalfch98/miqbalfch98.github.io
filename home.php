<p class="text-success">Halo, Saya Muhammad Iqbal Fachrezi 19670097. Selamat Datang di Sistem Informasi Geografis Pariwisata Kabupaten Pekalongan.
Jadi, Sistem Informasi Geografis adalah sistem 
informasi khusus yang mengelola data yang memiliki informasi spasial (bereferensi keruangan). 
Atau dalam arti yang lebih sempit, adalah sistem komputer yang memiliki kemampuan untuk membangun, 
menyimpan, mengelola dan menampilkan informasi berefrensi geografis, misalnya data yang diidentifikasi 
menurut lokasinya, dalam sebuah database. Para praktisi juga memasukkan orang yang membangun dan mengoperasikannya 
dan data sebagai bagian dari sistem ini.</p>
<p class="text-success"><strong>Sistem Informasi Geografis Pariwisata Kabupaten Pekalongan</strong> merupakan sistem
yang mengelolah tentang informasi geografis pada Pariwisata Kabupaten Pekalongan. Sistem ini menjelaskan
tentang penyebaran letak letak Tempat Pariwisata di Kabupaten Pekalongan, di sertakan dengan galeri tentang
beberapa tempat wisata yang ada.</p>
